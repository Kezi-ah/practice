let grade = "F";

switch (grade) {
  case "A":
    console.log("Excellent!");
    break;
  case "B":
    console.log("Good job");
    break;
  case "C":
    console.log("Fair, but you can improve");
    break;
  case "D":
    console.log("Needs improvement");
    break;
  case "F":
    console.log("Fail");
    break;
  default:
    console.log("Not a valid grade");
}


